
package t2.Objetos;

public class Gimnasios {

    private int id_g;
    private String Gimnasios;

    public Gimnasios(int id_g, String Gimnasios) {
        this.id_g = id_g;
        this.Gimnasios = Gimnasios;
    }

    public int getId_g() {
        return id_g;
    }

    public void setId_g(int id_g) {
        this.id_g = id_g;
    }

    public String getGimnasios() {
        return Gimnasios;
    }

    public void setGimnasios(String Gimnasios) {
        this.Gimnasios = Gimnasios;
    }
}
